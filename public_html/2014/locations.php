    <h2>Things To Do In M&uuml;nster</h2>
    <p align="justify">If you're one of our many lovely attendees traveling to M&uuml;nster from out of town, then of course, you'll be wanting to make the most of your time in this amazing city! For decades, creative types have flocked to M&uuml;nster to get inspired by its unrivalled energy and vibrance. We hope that you'll leave the GeoMundus Conf with your brain buzzing, not only from all of our incredible speakers, but from being immersed in such fantastic surroundings.
	<a class="image-link" href="http://www.muenster.de/stadt/tourismus/pdf/Museumsfuehrer_e.pdf">  Here are a few ideas </a>of stuff to check out while you're in town.</p>

    <ul class="locations">

	<li>
        <a class="image-link" href=""><img src="http://geomundus.org/2014/images/mus1.jpg" width="250" height="166" /></a>
        <h3>LWL-Museum f&uuml;r Kunst & Kultur</h3>
        <p>Domplatz 10<br />
D-48149&#160;M&uuml;nster</p>
<p>Pricing: &#8364; 3.50</p>
<p align="justify">Since its foundation in 1908 the Provincial Museum has been the centre for fine arts in Westphalia. It presents over a thousand years of art history, from the Middle Ages to modern times.</p>
<p><a href="http://www.lwl.org/LWL/Kultur/LWL-Landesmuseum-Muenster/">Website</a></p>


  	<li>
        <a class="image-link" href=""><img src="http://geomundus.org/2014/images/mus2.jpg" width="250" height="166" /></a>
        <h3>Kunstmuseum Pablo Picasso</h3>
        <p>Picassoplatz 1<br />
D-48149&#160;M&uuml;nster</p>
<p>Pricing: &#8364; 10.00</p>
<p align="justify">With over 780 lithographs, it houses a collection which is unique in the world, and which was further supplemented by a hundred etchings with the purchase of the "Suite Vollard" in Nov 2001.</p>
<p><a href="http://www.graphikmuseum-picasso-muenster.de/index.php?id=46&L=1">Website</a></p>


