	<h2>Making your Submission </h2>
	<div class="intro separated">
		<p>GeoMundus will be a scientific conference with peer-reviewed <a href="#paper-sub">papers</a> and <a href="#poster-sub">posters</a>, <strong>where young researchers, established researchers and industry present</strong> and discuss their work during the presentation sessions. They will have the opportunity to learn from and connect with their peers and innovators in Geo Science world. </p>
	</div>
	<div class="text">
		<div class="columns">
			<p>We are delighted to welcome paper and poster submissions for the upcoming Geomundus 2014 conference. The main purpose of this conference is to create networking opportunities for young researchers with professionals in geo-related science and industries, and academia alike.
			<br><br>GeoMundus will be a scientific conference with peer-reviewed papers and posters, where young researchers, established researchers and industry present and discuss their work during the presentation sessions. They will have the opportunity to learn from and connect with their peers and innovators in Geo Science world.
			<br><br>A the end of this page we have included a list of topics that are suitable for submission, please note that this is not an exhaustive list of topics. <a href="#topics">Suggested Topics -></a></p>
