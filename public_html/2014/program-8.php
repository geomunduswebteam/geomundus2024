        <div class="talk-info" id="workshop1">
          <h3>Workshop 1 - Games for Change</h3>
         <p align="justify" style="margin-bottom: 0px; align:justify">It is estimated that people spend around 3 billion hours a year playing games. By the age of 21 an average gamer living in a game-friendly culture has spent 10 000 hours playing. This is as much time as it takes to finish the high school or to become an expert in any field. Thus, average gamers by the age of 21 become masters of urgent optimism, social fabric, blissful productivity, and epic meaning.
<br>The best part is that these qualities are what it needs to save the world not only in games but in reality. The real world today suffers from hunger, poverty, climate change, global conflicts, obesity, and many other challenges that wait for talented problem-solvers to be conquered.
<br>The thing is, often reality cannot nearly offer as much fun as games. So what can we learn from games to help the virtual heroes to solve the real world problem? (Jane McGonigal, TEDx, 2010)
<br>In this workshop we want to embrace the ideas of Jane McGonigal and experiment with them by putting them into practice. We want to design and create games for change – the games that can help solving geographical problems. For this reason we want to think about future of location-based games and see what world-changing possibilities there are</p>
