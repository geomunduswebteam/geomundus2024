	<h2>About GeoMundus 2014</h2>
	<div class="columns">
	<p align="justify">The focus of this year's GeoMundus conference is <b>international cooperation to achieve sustainable development</b>. <br><br>The <strong><a href="http://www.un.org/millenniumgoals/environ.shtml" title="UN Millenium Goal 7">United Nations Millennium Goals</a></strong> identify the environmental sustainability as one of the eight goals. Reducing the loss of environmental resources including forests, increasing access to drinking water and reducing the loss of biodiversity are key targets to achieving this goal. <br><br>A key challenge in the world now is <strong>climate change</strong>. Scientific understanding of this phenomena and it's effects is very important. Many scientists and organizations are conducting disparate seperate programmes throughout the world. Geospatial Technologies can aid in the piecing this puzzle together. <br><br><br>International cooperation towards our understanding is crucial to combating climate change.
	In recent years disasters have become very frequent. GIS technology plays a <strong>critical role in the international response and coordination</strong> of relief actions in the wake of these disasters. Our increasing understanding of these phenomena has helped to create tools which are able predict these events and allow us to  make early decisions.<br><br>This conference will present some of the many ways geospatial technologies have been combined with ongoing scientific research into the effects of <strong>reducing global biodiversity</strong> and how our appetite for using environmental resources is affecting our environment. Alongside how it helps aid <strong>disaster response</strong> and <strong>combat climate change</strong>. Many global organisations recognise the effects of environmental changes, and these have dedicated geospatial and scientific research arms.
</p>
