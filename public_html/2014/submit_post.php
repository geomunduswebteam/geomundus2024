  <h1>Submissions<strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a class="image-link" style="font-size:27px; background: #c2f463; border-bottom: 1px dotted red;"  align="center" id="blink7" href="http://geomundus.org/submit_form.php">S&nbsp;u&nbsp;b&nbsp;m&nbsp;i&nbsp;t&nbsp;&nbsp;&nbsp;y&nbsp;o&nbsp;u&nbsp;r&nbsp;&nbsp;&nbsp;p&nbsp;a&nbsp;p&nbsp;e&nbsp;r &rarr;</a></strong>
	</h1>
    <div class="intro separated" align="justify" >
	 <p style="margin-bottom: 10px;margin-top: 10px; font-size:17px;">We are delighted to welcome paper and poster submissions for the upcoming Geomundus 2014 conference.<br>
The main purpose of this conference is to create networking opportunities for young researchers with professionals in geo-related science and industries, and academia alike.
<br>GeoMundus will be a scientific conference with peer-reviewed papers and posters, where <strong>young researchers, established researchers and industry </strong> present and discuss their work during the presentation sessions. They will have the opportunity to learn from and connect with their peers and innovators in Geo Science world.
<br>A the end of this page we have included a list of topics that are suitable for submission, please note that this is not an exhaustive list of topics. <b><em><a style="margin-bottom: 10px;margin-top: 10px; font-size:20px;" href="http://geomundus.org/topics">Suggested Topics</a></em></b>
</p>


    <h2 class="schedule-headline" style="margin-bottom: 10px;margin-top: 10px; font-size:17px; padding-top:10px;"><a href="http://geomundus.org/submit"><span>Paper submission</span></a> <small>|</small> Poster submission</h2>

<h1 style="margin-bottom: 10px;margin-top: 10px; font-size:20px;">Information to include</h1>
<div class="intro separated" align="justify" style="margin-bottom: 10px;margin-top: 10px; font-size:17px;">
   <ul><li>  Submit as <b>.pdf</b> and / or <b>zipped file</b> including Adobe Indesign file and sources
   </li><li> A0 printed poster (841mm Ã— 1189mm)
   </li><li> Clear, simple and uncluttered arrangement easy to read from 2m distance
   </li><li> Author/Presenter name(s)
   </li><li> Presentation title (10 words or less)
   </li><li> Institution / Company name
   </li><li> Brief presenter biography including picture (25 words or fewer)
   </li><li> <a href="http://geomundus.org/template_post.pdf"><em>Completed paper template</em></a>
   </li></ul>
</div>
<h1 style="margin-bottom: 10px;margin-top: 10px; font-size:20px;">General Advice</h1>
<div class="intro separated" align="justify">
<p style="margin-bottom: 10px;margin-top: 10px; font-size:17px;">It is useful to include 10mm bleeds for the poster to aid printing. Use a simple and clear font type. When using colors, make sure to have high contrast between them to keep everything legible. <b>Once you are notified of your poster's acceptance, it is your responsibility to print it in A0 size and bring it for display at the conference.</b>
<br><br>If you have any questions, please <a href="mailto:">contact us</a> and we'll get back to you as soon as possible. </p>
</div>


